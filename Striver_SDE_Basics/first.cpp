#include <iostream>
using namespace std;

int main() {
    int number;
    
    // Prompt the user to enter a number
    cout << "Enter a number: ";
    cin >> number;
    
    // Add 20 to the input number
    int result = number + 20;
    
    // Print the result
    cout << "Result: " << result << endl;
    
    return 0;
}
